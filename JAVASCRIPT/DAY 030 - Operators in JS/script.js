let a = 20;

let b = 10;

// Arithmetic Operators 

console.log("The value of a + b is ", a + b);
console.log("The value of a - b is ", a - b);
console.log("The value of a * b is ", a * b);
console.log("The value of a / b is ", a / b);
console.log("The value of a % b is ", a % b);
console.log("The value of a ** b is ", a ** b);
console.log("The value of a ++ is ", a++);
console.log("The value of a -- is ", a--);
console.log("The value of ++a is ", ++a);
console.log("The value of --a is ", --a);

// Assignment Operators

let c = b;
console.log(c);
